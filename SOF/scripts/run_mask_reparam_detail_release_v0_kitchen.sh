    DEFAULT_LAMBDA_RISK_SCALE="0.002"
    DEFAULT_LAMBDA_RISK_REST="0.010"
    DEFAULT_LAMBDA_SR_RISK_REST="0.012"
    DEFAULT_SR_RISK_BOOST="1.20"
