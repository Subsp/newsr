ITERATIONS="${ITERATIONS:-${DEFAULT_ITERATIONS}}"
MAX_VIEWS="${MAX_VIEWS:-${DEFAULT_MAX_VIEWS}}"
SR_MAX_VIEWS="${SR_MAX_VIEWS:-${MAX_VIEWS}}"
MIP_CLOSURE_MAX_VIEWS="${MIP_CLOSURE_MAX_VIEWS:-${SR_MAX_VIEWS}}"
