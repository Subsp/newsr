    ExtendedSettings = None

fields = set(getattr(GaussianRasterizationSettings, "_fields", ()))
required = {"inv_viewprojmatrix", "settings", "debug_data"}
ok = (
    ExtendedSettings is not None
    and bool(supports_merged_features())
    and required.issubset(fields)
)
if not ok:
    print("[merged-check] merged SOF rasterizer is unavailable in the active diff_gaussian_rasterization build.", flush=True)
    print(f"[merged-check] ExtendedSettings={'yes' if ExtendedSettings is not None else 'no'} supports_merged_features={bool(supports_merged_features())} fields_ok={required.issubset(fields)}", flush=True)
    raise SystemExit(1)
print("[merged-check] merged SOF rasterizer is available.", flush=True)
PY
  )
}

