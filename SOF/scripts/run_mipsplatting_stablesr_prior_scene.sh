validate_prepared_prior_reference_dir() {
  (
    export PREPARED_PRIOR_MANIFEST="${PREPARED_PRIOR_MANIFEST}"
    export EXPECTED_REFERENCE_DIR="${PREPARED_PRIOR_EXPECTED_REFERENCE_DIR}"
    "${PYTHON_BIN}" - <<'PY'
import json
import os
from pathlib import Path

manifest_path = Path(os.environ["PREPARED_PRIOR_MANIFEST"]).resolve()
expected_reference = Path(os.environ["EXPECTED_REFERENCE_DIR"]).resolve()
if not manifest_path.is_file():
    raise SystemExit(1)
manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
reference_dir = manifest.get("reference_dir")
if not reference_dir:
    print(f"[prepared-prior-ref] manifest missing reference_dir: {manifest_path}", flush=True)
    raise SystemExit(1)
actual_reference = Path(reference_dir).resolve()
print(
    f"[prepared-prior-ref] manifest reference_dir={actual_reference} expected={expected_reference}",
    flush=True,
)
if actual_reference != expected_reference:
    raise SystemExit(1)
PY
  )
}

